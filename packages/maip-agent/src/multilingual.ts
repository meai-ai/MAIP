/**
 * Multilingual & Voice Support.
 *
 * Implements the whitepaper's accessibility requirements:
 * - i18n framework for protocol messages and UI strings
 * - Voice adapter interfaces for TTS/STT integration
 * - Language detection and auto-translation pipeline
 * - Support for multiple character sets and RTL text
 */

/** Supported locale identifier (BCP 47). */
export type Locale = string;

/** A translation entry. */
export interface TranslationEntry {
  /** Message key. */
  key: string;
  /** Translations by locale. */
  translations: Record<Locale, string>;
}

/** Language detection result. */
export interface LanguageDetection {
  /** Detected language code (BCP 47). */
  language: Locale;
  /** Confidence score (0-1). */
  confidence: number;
  /** Script detected (e.g., "Latin", "Han", "Arabic"). */
  script: string;
  /** Whether the text is RTL. */
  isRTL: boolean;
}

/** Voice synthesis request. */
export interface TTSRequest {
  /** Text to synthesize. */
  text: string;
  /** Target language. */
  language: Locale;
  /** Voice ID (provider-specific). */
  voiceId?: string;
  /** Speaking rate (0.5-2.0, default 1.0). */
  rate?: number;
}

/** Voice synthesis result. */
export interface TTSResult {
  /** Audio data as base64. */
  audioBase64: string;
  /** Audio format. */
  format: "mp3" | "wav" | "opus";
  /** Duration in seconds. */
  durationSeconds: number;
}

/** Speech recognition request. */
export interface STTRequest {
  /** Audio data as base64. */
  audioBase64: string;
  /** Audio format. */
  format: "mp3" | "wav" | "opus" | "webm";
  /** Expected language (hint). */
  language?: Locale;
}

/** Speech recognition result. */
export interface STTResult {
  /** Transcribed text. */
  text: string;
  /** Detected language. */
  language: Locale;
  /** Confidence score (0-1). */
  confidence: number;
}

/** Translation request. */
export interface TranslationRequest {
  /** Text to translate. */
  text: string;
  /** Source language (auto-detect if not provided). */
  sourceLanguage?: Locale;
  /** Target language. */
  targetLanguage: Locale;
}

/** Translation result. */
export interface TranslationResult {
  /** Translated text. */
  translatedText: string;
  /** Detected source language. */
  sourceLanguage: Locale;
  /** Target language. */
  targetLanguage: Locale;
  /** Confidence in translation quality (0-1). */
  confidence: number;
}

/** Voice adapter interface — plug in any TTS/STT provider. */
export interface VoiceProvider {
  /** Provider name. */
  readonly name: string;
  /** Supported languages. */
  getSupportedLanguages(): Locale[];
  /** Synthesize speech from text. */
  synthesize(request: TTSRequest): Promise<TTSResult>;
  /** Recognize speech from audio. */
  recognize(request: STTRequest): Promise<STTResult>;
}

/** Translation provider interface. */
export interface TranslationProvider {
  /** Provider name. */
  readonly name: string;
  /** Supported language pairs. */
  getSupportedPairs(): Array<{ from: Locale; to: Locale }>;
  /** Translate text. */
  translate(request: TranslationRequest): Promise<TranslationResult>;
}

