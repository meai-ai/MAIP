/** RTL language codes. */
const RTL_LANGUAGES = new Set(["ar", "he", "fa", "ur", "ps", "sd", "yi"]);

/**
 * I18n — internationalization framework for MAIP protocol messages.
 */
export class I18n {
  private locale: Locale;
  private customMessages: Map<string, TranslationEntry> = new Map();

  constructor(locale: Locale = "en") {
    this.locale = locale;
    // Load built-in messages
    for (const entry of PROTOCOL_MESSAGES) {
      this.customMessages.set(entry.key, entry);
    }
  }

  /** Set the current locale. */
  setLocale(locale: Locale): void {
    this.locale = locale;
  }

  /** Get the current locale. */
  getLocale(): Locale {
    return this.locale;
  }

  /** Get a translated message. Falls back to English if not available. */
  t(key: string, locale?: Locale): string {
    const entry = this.customMessages.get(key);
    if (!entry) return key;
    return entry.translations[locale ?? this.locale] ?? entry.translations["en"] ?? key;
  }

  /** Add or update a translation entry. */
  addTranslation(key: string, locale: Locale, text: string): void {
    const existing = this.customMessages.get(key);
    if (existing) {
      existing.translations[locale] = text;
    } else {
      this.customMessages.set(key, { key, translations: { [locale]: text } });
    }
  }

  /** Check if a locale is RTL. */
  isRTL(locale?: Locale): boolean {
    const lang = (locale ?? this.locale).split("-")[0];
    return RTL_LANGUAGES.has(lang);
  }

  /** Get all available locales across all messages. */
  getAvailableLocales(): Locale[] {
    const locales = new Set<Locale>();
    for (const entry of this.customMessages.values()) {
      for (const locale of Object.keys(entry.translations)) {
        locales.add(locale);
      }
    }
    return [...locales].sort();
  }

  /** Detect language from text (simple heuristic-based). */
  detectLanguage(text: string): LanguageDetection {
    // Unicode range detection for script identification
    const hasHan = /[\u4e00-\u9fff]/.test(text);
    const hasHiragana = /[\u3040-\u309f]/.test(text);
    const hasKatakana = /[\u30a0-\u30ff]/.test(text);
    const hasHangul = /[\uac00-\ud7af]/.test(text);
    const hasArabic = /[\u0600-\u06ff]/.test(text);
    const hasCyrillic = /[\u0400-\u04ff]/.test(text);

    if (hasHiragana || hasKatakana) {
      return { language: "ja", confidence: 0.8, script: "Kana", isRTL: false };
    }
    if (hasHangul) {
      return { language: "ko", confidence: 0.9, script: "Hangul", isRTL: false };
    }
    if (hasHan) {
      return { language: "zh", confidence: 0.7, script: "Han", isRTL: false };
    }
    if (hasArabic) {
      return { language: "ar", confidence: 0.8, script: "Arabic", isRTL: true };
    }
    if (hasCyrillic) {
      return { language: "ru", confidence: 0.7, script: "Cyrillic", isRTL: false };
    }

    // Default to English for Latin script
    return { language: "en", confidence: 0.5, script: "Latin", isRTL: false };
  }
}

/**
 * No-op voice provider for testing / when no real TTS/STT is available.
 */
export class NoOpVoiceProvider implements VoiceProvider {
  readonly name = "noop";

  getSupportedLanguages(): Locale[] {
    return ["en"];
  }

  async synthesize(request: TTSRequest): Promise<TTSResult> {
    return {
      audioBase64: "",
      format: "mp3",
      durationSeconds: request.text.length * 0.06, // Rough estimate
    };
  }

  async recognize(_request: STTRequest): Promise<STTResult> {
    return {
      text: "",
      language: "en",
      confidence: 0,
    };
  }
}
